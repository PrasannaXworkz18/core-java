class Cleints{

   private int cleintsId;
   private String name;
   private String cleintsLocation;
   private long cleintsNumber;

  public void Cleints(){
	  
  }

  public void setCleintsId(int cleintsId){
	  this.cleintsId=cleintsId;
  }
  
  public int getCleintsId(){
	  return cleintsId;
  }

   public void setName(String name){
	   this.name=name;
   }

   public String getName(){
	   return name;
   }

  public void setCleintsLocation(String cleintsLocation){
	  this.cleintsLocation=cleintsLocation;
  }

  public String getCleintsLocation(){
	  return cleintsLocation;
  }

  public void setCleintsNumber(long cleintsNumber){
	  this.cleintsNumber=cleintsNumber;
  }

  public long getCleintsNumber(){
	  return cleintsNumber;
  }

}