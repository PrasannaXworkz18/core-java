class Refill{
	
	private int refillId;
	private String refillColor;
	private String refillBrand;
	private int refillLengthInCm;
	
	public void Refill(){
		
	}
	
	public void setRefillId(int refillId){
		this.refillId=refillId;
	}
	
	public int getRefillId(){
		return refillId;
	}
	
	public void setRefillColor(String refillColor){
		this.refillColor=refillColor;
	}
	
	public String getRefillColor(){
		return refillColor;
	}
	
	public void setRefillBrand(String refillBrand){
		this.refillBrand=refillBrand;
	}
	
	public String getRefillBrand(){
		return refillBrand;
	}
	
	public void setRefillLengthInCm(int refillLengthInCm){
		this.refillLengthInCm=refillLengthInCm;
	}
	
	public int getRefillLengthInCm(){
		return refillLengthInCm;
	}
	
}