class State{

   private String stateName;
   private String nameOfCm;
   private String capitalName;
   private int noOfDistrict;

  public void State(){
	  
  }

  public void setStateName(String stateName){
	  this.stateName=stateName;
  }
  
  public String getStateName(){
	  return stateName;
  }

  public void setNameOfCm(String nameOfCm){
	  this.nameOfCm=nameOfCm;
  }

  public String getNameOfCm(){
	  return nameOfCm;
  }

  public void setCapitalName(String capitalName){
	  this.capitalName=capitalName;
  }

  public String getCapitalName(){
	  return capitalName;
  }

  public void setNoOfDistrict(int noOfDistrict){
	  this.noOfDistrict=noOfDistrict;
  }

  public int getNoOfDistrict(){
	  return noOfDistrict;
  }

}